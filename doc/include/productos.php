<h2 class="page-header" style="margin-top:0;">Productos</h2>
<p>Son los <b>activos</b> que generan ingresos a nuestro negocio ya sean <u>tangibles/intagibles</u>.
<p>En <b>ventor</b> se han desarrollado estos de una manera simple, los cuales contienen los siguientes atributos.</p>
<ul>
	<li>
		<b>Nombre</b>: es el nombre del producto, trate de crear un nombre con una descripción completa.<br />
		Por ejemplo, en el nombre podemos incluir la marca y modelo: <i>Reloj Shockwave AR105 X1</i>.
	</li>
	<li>
		<b>Marca</b>: es la marca de nuestro producto, si no asignamos una, el sistema le pondrá automaticamente <b>S/M</b>.
		<br />
		Por ejemplo: <i>Shockwave</i>.
	</li>
	<li>
		<b>UDM</b>: es la unidad de medida de nuestro producto.<br />
		Por ejemplo: <i>UND</i>.
	</li>
	<li>
		<b>Stock Actual</b>: es el stock que tenemos actualmente para nuestro producto.<br />
		Por ejemplo: <i>10 UND</i>.
		<?php Info('Se require la extensión de <b>Control de Stock</b>'); ?>
	</li>
	<li>
		<b>Stock Mínimo</b>: es el stock mínimo que debe haber en nuestro inventario para poder mandar la alerta.<br />
		Por ejemplo: <i>2 UND</i>.
		<?php Info('Se require la extensión de <b>Control de Stock</b>'); ?>
	</li>
	<li>
		<b>Costo</b>: es lo que nos cuesta el producto.<br />
		Por ejemplo: <i>40.00</i>.
	</li>
	<li>
		<b>Precio de Venta</b>: es el precio con el que decidimos vender el producto.<br />
		Por ejemplo: <i>90.00</i>.
	</li>
</ul>