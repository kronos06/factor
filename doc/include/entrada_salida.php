<p>Los movimientos de nuestros productos son conocidos como entrada/salida.</p>
<h3>Entrada</h3>
<p>Una entrada hacemos referencia al <u>ingreso de un producto al almacén</u>, por lo tanto el sistema registrara el precio de compra y la cantidad con la que ingreso.</p>
<p>Para registrar dicha entrada hacemos click en el boton de acción y seleccionamos <b>Nueva Entrada</b>.</p>
<?php Imagen('btn_entrada_salida'); ?>
<h3>Salidas</h3>
<p>Estas son registradas cuando sucede en una <b>venta</b>, automaticamente se <u>creara una salida</u> para los productos que han sido afectados por la venta.</p>
<p>Para registrar dicha entrada hacemos click en el boton de acción y seleccionamos <b>Nueva Entrada</b>.</p>
<h3>Ajuste de Stock</h3>
<p>Suele suceder que nuestro stock no cuadra con el inventario físico, es aquí donde debemos hacer un ajuste de este.</p>
<p>Este no generará una entrada/salida para nuestro sistema.</p>
