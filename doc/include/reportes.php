<h2 class="page-header" style="margin-top:0;">Reportes</h2>
<p><b>Ventor</b> fue creado con la finalidad de ser un software que ayude en la mejora de deciciones, es por esto que los reportes han sido hecho con expertos en esta área.</p>
<p>Los reportes seran el <b>corazón</b> de nuestro negocio que nos ayudaran a tomar deciciones para optimizar nuestro negocio.</p>
<h3>Reporte por Periodo</h3>
<ul>
	<li>
		<b>Reporte de venta diario:</b> nos permite ver las <u>ventas/ganancias</u> de cada día en un mes determinado.
		<ul>
			<li>Podemos hacer click en un día para ver los productos que se han movido.</li>
			<li>Este incluye un reporte gráfico para una apreciación más simple.</li>
		</ul>
	</li>
	<li>
		<b>Reporte de venta mensual:</b> nos permite ver las <u>ventas/ganancias</u> de cada mes en un año determinado.
		<ul>
			<li>Este incluye un reporte gráfico para una apreciación más simple.</li>
		</ul>
	</li>
	<li>
		<b>Reporte de venta anual:</b> nos permite ver las <u>ventas/ganancias</u> de todo los años.
		<ul>
			<li>Este incluye un reporte gráfico para una apreciación más simple.</li>
		</ul>
	</li>
</ul>
